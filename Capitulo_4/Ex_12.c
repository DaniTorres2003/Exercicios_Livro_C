#include <stdio.h>

int main () {

int x;

scanf ("%d", &x);

switch (x) {
    case 1:
        printf ("\nDomingo\n");
        break;
    case 2:
        printf ("\nSegunda\n");
        break;
    case 3:
        printf ("\nTerca\n");
        break;
    case 4:
        printf ("\nQuarta\n");
        break;
    case 5:
        printf ("\nQuinta\n");
        break;
    case 6:
        printf ("\nSexta\n");
        break;
    case 7:
        printf ("\nSabado\n");
        break;
}


return 0;
}
